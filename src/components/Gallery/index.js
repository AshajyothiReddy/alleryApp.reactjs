class Gallery extends Component {
  state = {
    galleryImagesList: imagesList,
    initialImageUrl:
      'https://assets.ccbp.in/frontend/react-js/nature-mountain-with-pond-img.png',
    initialImageAltText: 'nature mountain with pond',
  }

  onClickThumbnail = id => {
    const galleryImagesList = this.state
    const requiredImageDetails = galleryImagesList.filter(
      each => each.id === id,
    )
    this.setState({
      initialImageUrl: requiredImageDetails.imageUrl,
      initialImageAltText: requiredImageDetails.imageAltText,
    })
    console.log(requiredImageDetails.imageUrl)
  }

  render() {
    const {initialImageUrl, initialImageAltText} = this.state
    const {galleryImagesList} = this.state
    return (
      <div className="app-container">
        <img
          src={initialImageUrl}
          className="gallery-image"
          alt="{initialImageAltText}"
        />
        <h1 className="heading">Nature Photography</h1>
        <p>Nature photography by Rahul</p>
        <ul className="thumbnail-list">
          {galleryImagesList.map(each => (
            <ThumbnailItem
              key={each.id}
              thumbnailDetails={each}
              onClickThumbnail={this.onClickThumbnail}
            />
          ))}
        </ul>
      </div>
    )
  }
}

export default Gallery
